package my_test;

public class LogicalOperator {
	
	public static void main(String[] args) {
		
		System.out.println("Logical operator result:" + (5>4 && 3>2));
		System.out.println("Logical operator result:" + (5>4 && 3>4));
		System.out.println("Logical operator result:" + (5>4 || 3>2));
		System.out.println("Logical operator result:" + (5>4 || 3>4));
		
	}

}
